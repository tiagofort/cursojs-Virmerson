class Calculadora {
    constructor(v1, v2) {
        this.v1 = v1
        this.v2 = v2
    }
    somar() {
        return this.v1 + this.v2
    }
    subtrair() {
        return this.v1 - this.v2
    }
    multiplicar() {
        return this.v1 * this.v2
    }
    dividir() {
        return this.v1 / this.v2
    }

}